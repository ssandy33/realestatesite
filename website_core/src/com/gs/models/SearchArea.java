package com.gs.models;

public class SearchArea extends BaseModel {
	private int id;
	private String name;
	
	public SearchArea() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
		
}
